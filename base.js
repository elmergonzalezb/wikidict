function get_attributes(trans) {
    var wortart = '[e]';
    $.ajax({
	type: 'GET',
        url: 'http://' + $('#to').val() + '.wiktionary.org/w/api.php',
	async: false,
	cache: false,
        timeout: 30000,
        data: {
            action: 'parse',
            prop: 'templates',
            format: 'json',
            page: trans
        },
        dataType: 'jsonp',
        success: function(data) {
		<!--alert('test');-->
		wortart = '[m]';
	},
	error: function(data) {
		alert('test');
	}
    });
	return wortart;
}

function show_result(data) {
    $("#result tbody > tr").remove();
    $("#result thead > tr").remove();
    $("#result thead").append('<tr><th width=50%>'+ iso639[$('#from').val()] +'</th><th>'+ iso639[$('#to').val()] +'</th></tr>');
    $.each(data.query.pages, function(page_id, page) {
        if (page.iwlinks === undefined) {
	    $('#result tbody').append('<tr><td colspan="2" align="center">Sorry, the page {inser searchterm here} exists, but no translation in your choosen language was given. If you want to add a translation, simply click <a target=new href=#>here</a>!</td></tr>');
            return false; // break
        }
        $.each(page.iwlinks, function(i, el) {
            var trans = el['*'];
            trans = trans.replace('Special:Search/', '').replace('_', ' ');
	    $('#result tbody').append('<tr><td><a target=new href=http://'+$('#from').val()+'.wiktionary.org/wiki/'+ $('#word').val() +'>' + $('#word').val() +'</a></td><td><a target=new href=http://'+$('#to').val()+'.wiktionary.org/wiki/'+ trans +'>' + trans + '</a> <font color=blue>' + get_attributes(trans) + '</font></td></tr>');
        });
    });
}

function setHistory(from, to, word) {
	var stateObj = {from: from, to: to, word: word };
	window.document.title = "WikiDict.cc - " + word;	
	history.pushState(stateObj, "WikiDict.cc - " + word, "?from="+ from + "&to=" + to + "&q=" + word);
}

function translate(from, to, word) {
	$('section > article').empty();
	$('#result').show();
	$('#word').val(word);
	$.ajax({
		url: 'http://' + from + '.wiktionary.org/w/api.php',
		data: {
			action: 'query',
			prop: 'iwlinks',
			format: 'json',
			iwlimit: 30,
			iwprefix: to,
			titles: word
		},
		dataType: 'jsonp',
	        success: show_result
    	});
    	return false;
}



$(window).load(function(){
  	$('form').submit(function() {
		var from = $('#from').val();
		var to = $('#to').val();
		var word = $('#word').val();

		setHistory(from, to, word);
		translate(from, to, word);
		return false;
	});
  	$('#word').focus();
});

window.onpopstate = function(event){
	translate(event.state['from'],event.state['to'],event.state['word']);
};
